public class OperasiMatematika {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        //penjumlahan
        System.out.println(a + b);

        //pengurangan
        System.out.println(a - b);

        //perkalian
        System.out.println(a * b);

        //pembagian
        System.out.println(a / b);

        //modulus -> sisa bagi
        System.out.println(a % b);

        //augmented assignment
        int c = 100;
        System.out.println(c += 10); // c = c + 10 = 110

        //unary assignment
        int d = 200;
        d++;
        System.out.println(d);

        d--;
        System.out.println(d);



    }
}
